    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //Cria um modelo default da tabela e atribui à tabela clientes
        DefaultTableModel dft = (DefaultTableModel) tblClientes.getModel();
        //Verifica se o usuario apertou em uma linha da tabela
        if (tblClientes.getSelectedRow() != -1) {
            BeansCliente beansCliente = new BeansCliente();
            DaoCliente daoCliente = new DaoCliente();
            
            //Seta no beans cliente o valor da coluna selecionado e a converte para inteiro
            beansCliente.setIdCliente((int) tblClientes.getValueAt(tblClientes.getSelectedRow(), 0));
            //Deleta o cliente
            daoCliente.deleteCliente(beansCliente);
            //Remove a linha selecionada pelo usuário
            dft.removeRow(tblClientes.getSelectedRow());
            //Carrega a tabela novamente
            carregarDadosTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para poder excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            //Conecta no banco de dados
            conn.getConnection();
            //Executa um SQL, seleciona tudo da tabela cliente onde o nome do cliente é igual ao que o usuário digitou, utilizando caracter coringa
            conn.executeSql("SELECT * FROM tbl_clientes WHERE nomecliente like'%"+txtPesquisa.getText()+"%'");
            //Pega o primeiro resultado
            conn.rs.first();
            //Seta os resultados nos campos
            txtId.setText(String.valueOf(conn.rs.getInt("idcliente")));
            txtNome.setText(conn.rs.getString("nomecliente"));
            txtCpf.setText(conn.rs.getString("cpfcliente"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não existe registro com esse nome.");
            Logger.getLogger(ViewLista.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Fecha conexão
        conn.closeConnection();
    }//GEN-LAST:event_btnBuscarActionPerformed

