    //Carregar dados da tabela
    public void carregarDadosTabela() {
        //Cria um modelo default da tabela e atribui à tabela clientes
        DefaultTableModel dft = (DefaultTableModel) tblClientes.getModel();
        //Seta as linhas geradas pelo modelo default
        tblClientes.setRowSorter(new TableRowSorter(dft));
        //Instancia da classe dao
        DaoCliente daoCliente = new DaoCliente();
        //Pecorre todo o beans cliente e vai adicionando na lista dao
        for (BeansCliente beans: daoCliente.getList()) {
            //Vai adicionando as linhas na tabela de acordo com os dados adquiridos do dao cliente
            dft.addRow(new Object[]{
                beans.getIdCliente(),
                beans.getNomeCliente(),
                beans.getCpfCliente(),
                beans.getNumeroCartao()
            });
        }
    }

